print ("Of the following three colors which is your favorite? (Red, Green, Blue)")
color = input ("Enter the color you like best ")

if color == "Red":
    print ("You picked Red.")
    print ("Red is a nice color")

elif color == "Green":
    print ("You picked Green.")
    print ("Green is a good color.")

elif color == "Blue":
    print ("You picked Blue.")
    print ("BLue is a fine color.")

else:
    print ("You did not pick one of the three colors")

input("\n\nPress the enter key to exit.")
